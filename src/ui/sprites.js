/** 도트 매트릭스 → SVG 문자열. 같은 행의 연속된 픽셀은 한 rect로 합친다. */
function bake(matrix, size = 16) {
  let rects = "";
  matrix.forEach((row, y) => {
    let x = 0;
    while (x < row.length) {
      if (row[x] === "#") {
        let w = 1;
        while (row[x + w] === "#") w += 1;
        rects += `<rect x="${x}" y="${y}" width="${w}" height="1"/>`;
        x += w;
      } else {
        x += 1;
      }
    }
  });
  return `<svg viewBox="0 0 ${size} ${size}" class="pixel-sprite" `
    + `shape-rendering="crispEdges" aria-hidden="true" focusable="false">`
    + `<g fill="currentColor">${rects}</g></svg>`;
}

export const SPRITES = {
  wave: bake(PLAY),
  rebirth: bake(CROSS),
  decorate: bake(HAMMER),
};
